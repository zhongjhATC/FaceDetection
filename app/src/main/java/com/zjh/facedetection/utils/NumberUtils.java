package com.zjh.facedetection.utils;

/**
 * 数字工具类
 * @author zhongjh
 */
public class NumberUtils {

    /**
     * float转double
     * @param num float数据
     * @return    double数据
     */
    public static double floatToDouble(float num) {
        return Double.parseDouble(String.valueOf(num));
    }
}
